    private void validerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerJButtonActionPerformed
      String nomVoilier = nomJTextField.getText();
      if(nomVoilier.equals("")){
          JOptionPane jop;
          jop=new JOptionPane();
          nomJTextField.setBorder(BorderFactory.createLineBorder(Color.RED, 2, rootPaneCheckingEnabled));
          jop.showMessageDialog(null,
    "nom du voilier vide!!!!",
    " warning",JOptionPane.WARNING_MESSAGE);
          return;
      }
      Classe classe =(Classe) classeJComboBox.getSelectedItem();
      Proprietaire proprietaire =(Proprietaire) proprietaireJComboBox.getSelectedItem();
      Bateau bateau = new Bateau(-1,nomVoilier,0,proprietaire,classe);
        try {
            BateauDAO.create(bateau);
        } catch (SQLException ex) {
            Logger.getLogger(CreationVoilier.class.getName()).log(Level.SEVERE, null, ex);
        }
           JOptionPane jop1;
           jop1=new JOptionPane();
           jop1.showMessageDialog(null,
    "le voilier à été créé.",
    " success",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_validerJButtonActionPerformed

