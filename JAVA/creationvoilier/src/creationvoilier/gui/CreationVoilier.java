    private void dialogValidJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dialogValidJButtonActionPerformed

            String prenom = nomPersoJTextField.getText();
            String nom = nomPersoJTextField.getText();
            String email = emailJTextField.getText();
            String addresse1= addresse1JTextField.getText();
            String cp=cpJTextField.getText();
            String ville = villeJTextfield.getText();
            String pays = paysJTextField.getText();
            String langue = langueJTextfield.getText();
            String tel = telephoneJTextField.getText();
            Club club = (Club) clubComboBox.getSelectedItem();
            if(prenom.isEmpty() || nom.isEmpty() || email.isEmpty() || addresse1.isEmpty()|| cp.isEmpty()||ville.isEmpty()||pays.isEmpty() || langue.isEmpty()||tel.isEmpty() ){
                JOptionPane jop;
                jop=new JOptionPane();
                jop.showMessageDialog(null,
                        "INFORMATION MANQUANTE!!!!!",
                        " warning",JOptionPane.WARNING_MESSAGE);
            }else{
                try {
                    Personne personne = new Personne(0,  prenom, nom,  email, null, addresse1,cp, ville, pays,  langue,  tel);
                    
                    int personneId=PersonneDAO.create(personne);
                    
                    personne.setId(personneId);
                    Proprietaire proprietaire = new Proprietaire(0,club,personne.getPersonneId(), personne.getPrenom(), personne.getNom(), personne.getEmail(), "", personne.getAddresse1(), personne.getAddresse2(), personne.getVille(), personne.getPays(), personne.getLangue(), personne.getTel());
                    ProprietaireDAO.create(proprietaire);
                    
                    JOptionPane jop1;
                    jop1=new JOptionPane();
                    jop1.showMessageDialog(null,
                            "le proprietaire a été créé",
                            " Success",JOptionPane.INFORMATION_MESSAGE);
                    
                    personneJDialog.dispose();
                } catch (SQLException ex) {
                    Logger.getLogger(CreationVoilier.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_dialogValidJButtonActionPerformed

